const express = require("express")
const auth = require("../middleware/auth")
const formController = require("../controller/form")
const validation = require("../middleware/validation")

module.exports = express
  .Router()
  .post("/forms", auth, validation("formCreate", "body"), formController.formCreate)
  .get("/forms", auth, formController.getAllForms)
  .get("/forms/:uuid", auth, formController.getOneForm)
  .put("/forms/:uuid", auth, validation("formUpdate", "body"), formController.formUpdate)
  .post("/forms/:uuid/submit", auth, formController.submitForm)
  .delete("/forms/:uuid", auth, formController.deleteForm)
  .get("/forms/:uuid/response", auth, formController.getFormResponse)
  .post("/forms/:uuid/copy", auth, formController.copyForm)
